class Solution {
public:
    int PalinArray(int a[], int n)
    {
    	// code here
    	for (int i=0; i<n; i++){
    	    int c=a[i], tmp=0;
    	    while(c>0){
    	        tmp=tmp*10+c%10;
    	        c=c/10;
    	    }
    	    if(tmp!=a[i])return 0;
    	}
    	return 1;
    }
};

